class GlyphIconEngine(QtGui.QIconEngine):
    def __init__(self, name: str, renderer: QtSvg.QSvgRenderer):
        super().__init__()
        self._name = name
        self._renderer = renderer

    def key(self) -> str:
        return f"GlyphIconEngine:{self._name}"

    def _cacheKey(self, size: QtCore.QSize, mode: QtGui.QIcon.Mode,
                  state: QtGui.QIcon.State) -> str:
        return f"{self.key()}/{size.width()}x{size.height()}"

    def _render(self, size: QtCore.QSize, mode: QtGui.QIcon.Mode,
                state: QtGui.QIcon.State) -> QtGui.QPixmap:
        svgr = self._renderer
        pm = QtGui.QPixmap(size)
        painter = QtGui.QPainter(pm)
        svgr.render(painter)
        painter.end()
        return pm

    def pixmap(self, size: QtCore.QSize, mode: QtGui.QIcon.Mode,
               state: QtGui.QIcon.State) -> QtGui.QPixmap:
        pm = QtGui.QPixmap()
        key = self._cahceKey(size, mode, state)
        if not QtGui.QPixmapCache.find(key, pm):
            pm = self._render(mode, state)
            QtGui.QPixmapCache.insert(key, pm)
        return pm

    def paint(self, painter: QtGui.QPainter, rect: QtCore.QRect,
              mode: QtGui.QIcon.Mode, state: QtGui.QIcon.State) -> None:
        pm_size = rect.size()
        if painter.device():
            pm_size *= painter.device().devicePixelRatioF()
        pm = self.pixmap(pm_size, mode, state)
        painter.drawPixmap(rect, pm)


def forName(name: str) -> Glyph:
    if "." in name:
        family, name = name.rsplit(".", 1)
    else:
        family = DEFAULT_FAMILY

    if family == "iconfx":
        glyph_type = IconFX
    elif family == "font_awesome" or family == "font_awesome.outlined":
        glyph_type = FontAwesome
    # elif family == "font_awesome.solid":
    #     glyph_type = FontAwesomeSolid
    elif family == "material" or family == "material.outlined":
        glyph_type = MaterialIcons
    # elif family == "material.solid":
    #     glyph_type = MaterialIconsSolid
    else:
        raise KeyError(f"Unkown glyph family: {family!r}")

    return glyph_type.__members__.get(name)


