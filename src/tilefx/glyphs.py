class GlyphIO:
    def svgBytes(self, name: str) -> bytes:
        raise NotImplementedError


class ResourceGlyphIO(GlyphIO):
    def __init__(self, root: importlib.abc.Traversable):
        self._root = root

    def svgBytes(self, name: str) -> bytes:
        resource = self._root / name
        return resource.read_bytes()


class FileGlyphIO(GlyphIO):
    def __init__(self, basepath: Union[str, pathlib.Path]):
        self._base = pathlib.Path(basepath)

    def svgBytes(self, name: str) -> bytes:
        path = self._base / name
        return path.read_bytes()


class ZipGlyphIO(GlyphIO):
    def __init__(self, zip_path: Union[str, pathlib.Path]):
        self._zipfile = zipfile.ZipFile(zip_path)

    def svgBytes(self, name: str) -> bytes:
        return self._zipfile.read(name)


class GlyphSource:
    def __init__(self, io: GlyphIO):
        if not isinstance(io, GlyphIO):
            raise TypeError(f"{io!r} is not a GlyphIO object")
        self._io = io

    def renderer(self, name: str) -> QtSvg.QSvgRenderer:
        svg = self._io.svgBytes(name)
        rend = QtSvg.QSvgRenderer()
        # rend.setAspectRatioMode(rend.KeepAspectRatio)
        rend.load(svg)
        return rend

    def icon(self, name: str) -> QtGui.QIcon:
        rend = self.renderer(name)
        engine = GlyphIconEngine(name, rend)
        icon = QtGui.QIcon(engine)
        icon.setIsMask(True)
        return icon


